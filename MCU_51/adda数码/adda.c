uint getdata(uchar tong)
{
	uchar i,ming;
	uint sj=0;
	tong=tong<<4;
	clk2543=0;
	cs2543=0;
	ming=tong|0x04;
	for(i=0;i<8;i++)
	{
		clk2543=0;
		if(sdo2543)
			sj=sj|0x01;
		sdi2543=(bit)(ming&0x80);
		clk2543=1;
		_nop_();_nop_();_nop_();
		clk2543=0;
		_nop_();_nop_();_nop_();
		ming=ming<<1;
		sj<<=1;
	}
	cs2543=1;
	sj>>=1;
	return sj;
}
